namespace BaroquenMelody.Library.Compositions.Enums;

/// <summary>
///    Indicates the voice being used to play a note.
/// </summary>
public enum Voice : byte
{
    /// <summary>
    ///     The first voice.
    /// </summary>
    Soprano,

    /// <summary>
    ///    The second voice.
    /// </summary>
    Alto,

    /// <summary>
    ///   The third voice.
    /// </summary>
    Tenor,

    /// <summary>
    ///  The fourth voice.
    /// </summary>
    Bass
}
