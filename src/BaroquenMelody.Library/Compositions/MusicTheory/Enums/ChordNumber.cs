namespace BaroquenMelody.Library.Compositions.MusicTheory.Enums;

/// <summary>
///     Represents the number of a chord in a key.
/// </summary>
internal enum ChordNumber : byte
{
    /// <summary>
    ///     An unknown chord.
    /// </summary>
    Unknown = 0,

    /// <summary>
    ///     The one chord of a key.
    /// </summary>
    I,

    /// <summary>
    ///     The two chord of a key.
    /// </summary>
    II,

    /// <summary>
    ///     The three chord of a key.
    /// </summary>
    III,

    /// <summary>
    ///     The four chord of a key.
    /// </summary>
    IV,

    /// <summary>
    ///     The five chord of a key.
    /// </summary>
    V,

    /// <summary>
    ///     The six chord of a key.
    /// </summary>
    VI,

    /// <summary>
    ///     The seven chord of a key.
    /// </summary>
    VII
}
