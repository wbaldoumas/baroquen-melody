namespace BaroquenMelody.Library.Composition.Enums;

/// <summary>
///    Indicates the voice being used to play a note.
/// </summary>
internal enum Voice : byte
{
    /// <summary>
    ///     The highest voice.
    /// </summary>
    Soprano,

    /// <summary>
    ///    The second highest voice.
    /// </summary>
    Alto,

    /// <summary>
    ///   The second lowest voice.
    /// </summary>
    Tenor,

    /// <summary>
    ///  The lowest voice.
    /// </summary>
    Bass
}
